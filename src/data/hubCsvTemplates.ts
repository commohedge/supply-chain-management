export function buildTemplateCsv(headers: readonly string[], exampleRow: string[]): string {
  const esc = (s: string) => {
    const t = String(s);
    if (/[",\n\r]/.test(t)) return `"${t.replace(/"/g, '""')}"`;
    return t;
  };
  return [headers.join(","), exampleRow.map(esc).join(",")].join("\r\n");
}

